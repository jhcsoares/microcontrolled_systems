;RAM
LEDS_STATE				EQU	   0x20000400
LEDS_INCREMENT			EQU	   0x20000401
UPDATE_FLAG				EQU	   0x20000406

; -------------------------------------------------------------------------------
; Área de Código - Tudo abaixo da diretiva a seguir será armazenado na memória de 
;                  código
        AREA    |.text|, CODE, READONLY, ALIGN=2

		; Se alguma função do arquivo for chamada em outro arquivo	
        EXPORT UpdateLeds
		IMPORT  SysTick_Wait1ms

;--------------------------------------------------------------------------------
;UpdateLeds
UpdateLeds	
	LDR R4, =LEDS_STATE
	LDRB R2, [R4]
	LDR R5, =GPIO_PORTA_AHB_DATA_R
	LDR R6, =GPIO_PORTQ_DATA_R
	
	CMP R2, #0
	ITTTT EQ
		MOVEQ R3, #2_10000000
		MOVEQ R4, #2_00000001
		STREQ R3, [R5]
		STREQ R4, [R6]
	
	CMP R2, #1
	ITTTT EQ
		MOVEQ R3, #2_01000000
		MOVEQ R4, #2_00000010
		STREQ R3, [R5]
		STREQ R4, [R6]
	
	CMP R2, #2
	ITTTT EQ
		MOVEQ R3, #2_00100000
		MOVEQ R4, #2_00000100
		STREQ R3, [R5]
		STREQ R4, [R6]
	
	CMP R2, #3
	ITTTT EQ
		MOVEQ R3, #2_00010000
		MOVEQ R4, #2_00001000
		STREQ R3, [R5]
		STREQ R4, [R6]
	
	;SATURATE TRANSISTOR
	MOV R3, #2_00100000
	LDR R4, =GPIO_PORTP_DATA_R
	STR R3, [R4]
	
	MOV R0, #1
	PUSH {LR}
	BL SysTick_Wait1ms
	POP {LR}
	
	;TURN OFF LEDS
	MOV R3, #2_00000000
	LDR R4, =GPIO_PORTP_DATA_R
	STR R3, [R4]
	
	MOV R0, #1
	PUSH {LR}
	BL SysTick_Wait1ms
	POP {LR}
		
	;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;UPDATE STATE;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
	LDR R0, =UPDATE_FLAG
	LDRB R1, [R0]
	CMP R1, #0
	BXEQ LR
	
	LDR R3, =LEDS_INCREMENT
	LDR R4, =LEDS_STATE
	LDRB R2, [R4]
	
	;CHECKING CURRENT STATE AND UPDATING LEDS_INCREMENT
	CMP R2, #3
	ITT EQ
		MOVEQ R0, #0
		STRBEQ R0, [R3]
	
	CMP R2, #0
	ITT EQ
		MOVEQ R0, #1
		STRBEQ R0, [R3]
	
	LDRB R0, [R3]
	LDRB R1, [R4]
	
	CMP R0, #0
	ITTEE EQ
		SUBEQ R2, R1, #1
		STRBEQ R2, [R4]
		ADDNE R2, R1, #1
		STRBNE R2, [R4]
	
	BX LR
		
	ALIGN                           ; garante que o fim da seção está alinhada 
    END  
	